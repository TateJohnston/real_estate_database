"use strict";
const Models = require("../models");

const getSalesHistory = (res) => {
  Models.SaleHistory.findAll({})
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};
const createSaleHistory = (data, res) => {
  Models.SaleHistory.create(data)
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};

const deleteSaleHistory = (req, res) => {
  Models.SaleHistory.destroy({ where: { id: req.params.id } })
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};

module.exports = {
  getSalesHistory,
  createSaleHistory,
  deleteSaleHistory,
};
