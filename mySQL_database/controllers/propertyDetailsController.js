"use strict";
const Models = require("../models");

const getPropertyDetails = (res) => {
  Models.PropertyDetails.find({})
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};

const createPropertyDetails = (data, res) => {
  Models.PropertyDetails.create(data)
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};

const updatePropertyDetails = (req, res) => {
  Models.PropertyDetails.update(req.body, {
    where: { id: req.params.id },
    returning: true,
  })
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};

const deletePropertyDetails = (req, res) => {
  Models.PropertyDetails.destroy({ where: { id: req.params.id } })
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};
module.exports = {
  getPropertyDetails,
  updatePropertyDetails,
  createPropertyDetails,
  deletePropertyDetails,
};
